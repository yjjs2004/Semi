package net.person.db;

public class Faq {

	private String FAQ_ID;
	private String FAQ_KINDS;
	private String FAQ_SUBJECT;
	private String FAQ_CONTENT;
	private String FAQ_DATE;
	private String FAQ_COMPLETE;
	public String getFAQ_ID() {
		return FAQ_ID;
	}
	public void setFAQ_ID(String fAQ_ID) {
		FAQ_ID = fAQ_ID;
	}
	public String getFAQ_KINDS() {
		return FAQ_KINDS;
	}
	public void setFAQ_KINDS(String fAQ_KINDS) {
		FAQ_KINDS = fAQ_KINDS;
	}
	public String getFAQ_SUBJECT() {
		return FAQ_SUBJECT;
	}
	public void setFAQ_SUBJECT(String fAQ_SUBJECT) {
		FAQ_SUBJECT = fAQ_SUBJECT;
	}
	public String getFAQ_CONTENT() {
		return FAQ_CONTENT;
	}
	public void setFAQ_CONTENT(String fAQ_CONTENT) {
		FAQ_CONTENT = fAQ_CONTENT;
	}
	public String getFAQ_DATE() {
		return FAQ_DATE;
	}
	public void setFAQ_DATE(String fAQ_DATE) {
		FAQ_DATE = fAQ_DATE;
	}
	public String getFAQ_COMPLETE() {
		return FAQ_COMPLETE;
	}
	public void setFAQ_COMPLETE(String fAQ_COMPLETE) {
		FAQ_COMPLETE = fAQ_COMPLETE;
	}
}
